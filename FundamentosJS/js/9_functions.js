// Funciones

// Declaración de funcion "suma2mas2()"
function suma2mas2() {
    console.log(2 + 2);
}
// Llamada (ejecución) de la función
suma2mas2(); // 4

// Función con Parámetros
function sumaParam(num1, num2) {
    console.log(num1 + num2);
}
// Llamada (ejecución) de la función, con parámetros
sumaParam(2, 5); // 7

// Función con Parámetros inicializados
function sumaParam(num1 = 0, num2 = 0) {
    console.log(num1 + num2);
}
// Llamada (ejecución) de la función, con parámetros
sumaParam(6); // 6

